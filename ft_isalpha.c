#include "libft.h"
/*
** Returns 1 if c is an alphabet character.
**/
int	ft_isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	else
		return (0);
}
