#include "libft.h"
/*
** Clears a string by setting all characters to '\0'.
*/
void	ft_strclr(char *s)
{
	int	i;

	i = 0;
	if (s)
	{
		while (s[i])
		{
			s[i] = '\0';
			i++;
		}
	}
}
